let x = 2.30;
let y;
let z;

Math.floor(x); // rounds down to the nearest integer
Math.ceil(x); // rounds up to the nearest integer
Math.round(x); // rounds to the nearest integer
Math.abs(x); // returns the absolute value of a number
Math.sqrt(x); // returns the square root of a number
Math.pow(x, y); // returns x to the power of y
Math.random(); // returns a random number between 0 and 1
Math.PI; // returns the value of pi
Math.max(x, y); // returns the maximum of two numbers
Math.min(x, y); // returns the minimum of two numbers
Math.max(x, y, z); // returns the maximum of three numbers
Math.min(x, y, z); // returns the minimum of three numbers
Math.trunc(x); // returns the integer part of a number
Math.sign(x); // returns the sign of a number (-1, 0, or 1)
